package ReviewClass04;

public class ForLoopDemo1 {

	public static void main(String[] args) {
		//any piece of logic can be done with while loop or for loop
		//with the for loop the code looks cleaner
		//when you do not know when to stop the conditions we use WHILE LOOP
		//otherwise we can use the FOR LOOP
		//we use FOR when we know how many times we want to repeat block of code
		//for loop has 3 parts exactly the same with the WHILE LOOP but they are written differently
		//starting loop,stopping condition goes inside the for loop and then the increment point
		
		for(int x=1; x<=5;x++) {
			System.out.println(x);
		}

	}

}
